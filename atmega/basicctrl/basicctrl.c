#include <avr/io.h>
#include <avr/wdt.h>
#include <avr/interrupt.h>
#include <avr/power.h>
#include <stdio.h>

#include "util.h"
#include "led.h"
#include "usbio.h"

#include "relay.h"
#include "keypad.h"

uint8_t current_relay = 0;

void handle_cmd(uint8_t cmd)
{
  switch(cmd) {
  case '0':
  case '1':
  case '2':
  case '3':
  case '4':
  case '5':
  case '6':
  case '7':
    current_relay = cmd - '0'; printf("output %d selected\r\n", current_relay); return;
  case 'i': relay_on(current_relay); break;
  case 'o': relay_off(current_relay); break;
  case 't': relay_toggle(current_relay); break;
  case '-': break;
  default: printf("error\r\n"); return;
  }
  printf("state of output %d is now: %d\r\n", current_relay, relay_get(current_relay));
}

int main(void)
{
  MCUSR &= ~(1 << WDRF);
  wdt_disable();

  cpu_init();
  led_init();
  usbio_init();
  relay_init();
  keypad_init();
  sei();

  for(;;) {
    int16_t BytesReceived = usbio_bytes_received();
    while(BytesReceived > 0) {
      int ReceivedByte = fgetc(stdin);
      if(ReceivedByte != EOF) {
        handle_cmd(ReceivedByte);
      }
      BytesReceived--;
    }

    usbio_task();
  }
}
